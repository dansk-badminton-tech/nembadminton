    <h2>4. Deling af data (Data Sharing)</h2>
    <p>
        Vi <strong>sælger ikke</strong> dine personoplysninger og deler dem ikke med tredjeparter
        i markedsføringsøjemed. Dine oplysninger behandles fortroligt og deles kun med de
        databehandlere, der er nødvendige for at drive tjenesten:
    </p>
    <ul>
        <li><strong>Hostingudbyder</strong> – opbevaring og drift af serveren og databasen.</li>
        <li><strong>E-mailudbyder</strong> – udsendelse af nødvendige system- og transaktions-e-mails.</li>
        <li><strong>Fejlovervågning</strong> – teknisk fejllogning, så vi kan opdage og rette fejl.</li>
        <li><strong>Anonym statistik</strong> – cookieløs, anonymiseret brugsstatistik (Umami), der ikke kan henføres til dig som person.</li>
    </ul>
    <p>
        Disse databehandlere må kun behandle oplysningerne efter vores instruks. Vi kan desuden
        videregive oplysninger, hvis vi er retligt forpligtede hertil.
    </p>

    <h2>5. Opbevaring og beskyttelse af data (Data Storage &amp; Protection)</h2>
    <ul>
        <li>Data opbevares i en database hos vores hostingudbyder inden for EU/EØS.</li>
        <li>Al kommunikation med tjenesten sker krypteret via HTTPS/TLS.</li>
        <li>Adgangskoder gemmes aldrig i klartekst, men opbevares som sikre hashværdier.</li>
        <li>Adgang til data er begrænset til det nødvendige for drift og vedligeholdelse.</li>
    </ul>

    <h2>6. Opbevaringsperiode og sletning (Data Retention &amp; Deletion)</h2>
    <p>
        Vi opbevarer dine oplysninger, så længe du har en aktiv konto på Nembadminton.dk.
    </p>

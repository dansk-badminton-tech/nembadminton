    </p>

    <h2>2. Hvilke Google-data vi tilgår (Data Accessed)</h2>
    <p>
        Når du vælger at logge ind eller oprette en bruger med <em>"Log ind med Google"</em>,
        anmoder vi udelukkende om følgende Google-scopes via Google OpenID Connect:
    </p>
    <ul>
        <li><code>openid</code> – din unikke Google konto-identifikator (Google-bruger-ID)</li>
        <li><code>email</code> – din e-mailadresse og om den er bekræftet</li>
        <li><code>profile</code> – dit navn og eventuelt dit offentlige profilbillede</li>
    </ul>
    <p>
        Vi anmoder <strong>ikke</strong> om adgang til følsomme eller begrænsede Google-data såsom
        Gmail, Google Drev, Kalender, Kontakter eller andre Google-tjenester.
    </p>

    <h2>3. Hvordan vi bruger dine data (Data Usage)</h2>
    <p>De modtagne Google-oplysninger anvendes alene til følgende formål:</p>
    <ul>
        <li>At oprette og identificere din brugerkonto på Nembadminton.dk.</li>
        <li>At logge dig sikkert ind uden brug af adgangskode (autentificering).</li>
        <li>At knytte din Google-konto til din eksisterende Nembadminton-konto, så du kan vælge login-metode.</li>
        <li>At vise dit navn og din e-mail i applikationen, så du og din klub kan genkende din profil.</li>
    </ul>
    <p>
        Vi bruger <strong>ikke</strong> dine Google-data til reklame, profilering eller salg, og vi
        anvender dem ikke til andre formål end dem, der er nævnt ovenfor.
    </p>

